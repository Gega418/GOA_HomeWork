// 1. რა არის HTTP? (HyperText Transfer Protocol)
// HTTP არის ჰიპერტექსტის გადაცემის პროტოკოლი. ის არის ინტერნეტის "ძველი" სტანდარტი, რომლის დროსაც ინფორმაცია კლიენტსა და სერვერს შორის მოძრაობს ღია, დაუშიფრავი ტექსტის (Plain Text) სახით.

// დანიშნულება: ვებ-გვერდების HTML დოკუმენტების და სხვა რესურსების გადმოწერა სერვერიდან ბრაუზერში.

// მთავარი მინუსი: ვინაიდან მონაცემები არ არის დაშიფრული, ნებისმიერ ჰაკერს ან მესამე პირს (მაგალითად, საჯარო Wi-Fi-ს პროვაიდერს), რომელიც თქვენს ინტერნეტ ტრაფიკს "უსმენს", შეუძლია მარტივად წაიკითხოს ყველაფერი, რასაც საიტზე წერთ (პაროლები, ბარათის მონაცემები და ა.შ.).

// 2. რა არის HTTPS? (HyperText Transfer Protocol Secure)
// HTTPS არის იგივე HTTP, ოღონდ ბოლოში "S" ნიშნავს Secure-ს (უსაფრთხოს). ეს არის თანამედროვე ინტერნეტის სტანდარტი. მონაცემების გადაცემამდე ბრაუზერი და სერვერი ინფორმაციას შიფრავენ სპეციალური უსაფრთხოების პროტოკოლის — SSL/TLS-ის საშუალებით.

// დანიშნულება: მონაცემების უსაფრთხო და დაცული მიმოცვლა ინტერნეტში.

// როგორ მუშაობს: საიტზე შეყვანილი პაროლი ან საკრედიტო ბარათის ნომერი ინტერნეტში მოგზაურობს არა როგორც ტექსტი, არამედ როგორც გაურკვეველი სიმბოლოების გრძელი კოდი. მისი წაკითხვა და "გაშიფვრა" მხოლოდ იმ კონკრეტულ სერვერს შეუძლია, ვისთანაც კავშირი გაქვს დამყარებული.